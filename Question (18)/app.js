// Storing Locations 
var placeVisit = ["China", "Japan", "Austrailia", "Uk", "Turkey"];
console.log(placeVisit);
// Printing array in Alphabetical order
var copyplaceVisit = placeVisit.slice();
console.log(copyplaceVisit.sort());
// Print Original Array
console.log(placeVisit);
// Printing in reverse Alphabetical order
console.log(copyplaceVisit.reverse());
// Print Original Array
console.log(placeVisit);
// Reversing original array
placeVisit = placeVisit.reverse();
console.log(placeVisit);
// Again Reversing original array
placeVisit = placeVisit.reverse();
console.log(placeVisit);
// Sorting Original Array 
placeVisit = placeVisit.sort();
console.log(placeVisit);
// Reverse Alphabetical Order Original Array 
placeVisit = placeVisit.reverse();
console.log(placeVisit);
